package org.devgateway.rdi.tanzania.domain;

import com.fasterxml.jackson.annotation.JsonIgnore;

import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.ManyToOne;

/**
 * @author Sebastian Dimunzio
 */

@Entity
public class ServiceAreaPopulation {

    @GeneratedValue(strategy = GenerationType.AUTO)
    @javax.persistence.Id
    private Long id;

    @JsonIgnore
    @ManyToOne(targetEntity = Facility.class)
    private Facility facility;
    private Integer year;
    private Double value;
    private String gender;
    private String age;

    @ManyToOne(targetEntity = Item.class)
    Item genderItem;

    @ManyToOne(targetEntity = Item.class)
    Item ageItem;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public Facility getFacility() {
        return facility;
    }

    public void setFacility(Facility facility) {
        this.facility = facility;
    }

    public Integer getYear() {
        return year;
    }

    public void setYear(Integer year) {
        this.year = year;
    }

    public Double getValue() {
        return value;
    }

    public void setValue(Double value) {
        this.value = value;
    }

    public String getGender() {
        return gender;
    }

    public void setGender(String gender) {
        this.gender = gender;
    }

    public String getAge() {
        return age;
    }

    public void setAge(String age) {
        this.age = age;
    }

    public Item getGenderItem() {
        return genderItem;
    }

    public void setGenderItem(Item genderItem) {
        this.genderItem = genderItem;
    }

    public Item getAgeItem() {
        return ageItem;
    }

    public void setAgeItem(Item ageItem) {
        this.ageItem = ageItem;
    }
}
